  loadMap() {
    this.tileSheet.background = this.tileSheet.background || '#333';
    this.tileSize = this.tileSheet.tileSize || 16;
    this.tileSheet.width = 0;
    this.tileSheet.heignt = 0;

    this.tileSheet.keys.forEach((key) => {
      this.tileSheet.map.forEach((row, y) => {
        this.tileSheet.height = Math.max(this.tileSheet.height, y);

        row.forEach((tile, x) => {
          this.tileSheet.width = Math.max(this.tileSheet.width, x);
          if (tile === key.id) {
            this.tileSheet.map[y][x] = key;
          }
        });
      });
    });

    this.tileSheet.widthP = this.tileSheet.width * this.tileSheet.tileSize;
    this.tileSheet.heightP = this.tileSheet.height * this.tileSheet.tileSize;

    this.player.x *= this.tileSheet.tileSize || 0;
    this.player.y *= this.tileSheet.tileSize || 0;
  }

  getTile(x, y) {
    return (this.tileSheet.map[y] && this.tileSheet.map[y][x]) ? this.tileSheet.map[y][x] : 0;
  }

  drawTile(x, y, tile, context) {
    const color = this.tileSheet.keys.filter((item) => {
      if (tile === item.id) {
        return item.color;
      }
    });
    context.fillStyle = color[0].color;
    context.fillRect(x, y, this.tileSheet.tileSize, this.tileSheet.tileSize);
  }

  drawMap(context, fore) {
    for (let i = 0; i < this.tileSheet.map.length; i++) {
      for (let j = 0; j < this.tileSheet.map[i].length; j++) {
        if ((!fore && !this.tileSheet.map[i][j].fore) || (fore && this.tileSheet.map[i][j].fore)) {
          const tileX = (j * this.tileSheet.tileSize) - this.camera.x;
          const tileY = (i * this.tileSheet.tileSize) - this.camera.y;

          if (tileX < -this.tileSheet.tileSize || tileY < -this.tileSheet.tileSize || tileX > this.viewport.x || tileY > this.viewport.y);

          this.drawTile(tileX, tileY, this.tileSheet.map[i][j], context);
        }
      }
    }

    if (!fore) {
      this.drawMap(context, true);
    }
  }

  drawPlayer(context) {
    context.fillStyle = this.player.color;
    context.beginPath();
    context.arc(this.player.x + this.tileSheet.tileSize / 2 - this.camera.x,
      this.player.y + this.tileSheet.tileSize / 2 - this.camera.y,
      this.tileSheet.tileSize / 2 - 1,
      0,
      Math.PI * 2);
    context.fill();
  }

  movePlayer() {
    const tile = this.getTile(Math.round(this.player.x / this.tileSheet.tileSize), 
      Math.round(this.player.y / this.tileSheet.tileSize));

    if (tile === 3) {
      this.player.speedX += this.tileSheet.keys.gravity.x;
      this.player.speedY += this.tileSheet.keys.gravity.y;
      this.player.speedX *= this.tileSheet.keys.friction.x;
      this.player.speedY *= this.tileSheet.keys.friction.y;
    } else {
      this.player.speedX += this.gravity.x;
      this.player.speedY += this.gravity.y;
    }
  }
